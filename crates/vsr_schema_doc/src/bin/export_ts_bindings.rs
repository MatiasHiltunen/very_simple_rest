use std::env;
use std::fs;
use std::path::PathBuf;

use ts_rs::{Config, TS};
use vsr_schema_doc::{ServiceDocument, ServiceInputDocument};

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR")?);
    let out_dir = manifest_dir.join("../../packages/vsr/src/generated");
    fs::create_dir_all(&out_dir)?;

    let config = Config::default().with_out_dir(&out_dir);
    ServiceInputDocument::export_all(&config)?;
    ServiceDocument::export_all(&config)?;

    let mut exports = fs::read_dir(&out_dir)?
        .filter_map(|entry| entry.ok())
        .filter_map(|entry| {
            let path = entry.path();
            let file_name = path.file_name()?.to_str()?;
            if file_name == "index.ts"
                || path.extension().and_then(|ext| ext.to_str()) != Some("ts")
            {
                return None;
            }

            Some(file_name.trim_end_matches(".ts").to_owned())
        })
        .collect::<Vec<_>>();
    exports.sort();

    fs::write(
        out_dir.join("index.ts"),
        std::iter::once(
            "// Generated by `cargo run -p vsr_schema_doc --bin export_ts_bindings`.".to_owned(),
        )
        .chain(
            exports
                .iter()
                .map(|name| format!("export type * from './{name}';")),
        )
        .chain(std::iter::once(String::new()))
        .collect::<Vec<_>>()
        .join("\n"),
    )?;

    Ok(())
}
